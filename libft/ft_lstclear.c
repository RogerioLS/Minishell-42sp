#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*node;

	while (*lst)
	{
		node = *lst;
		*lst = node->next;
		ft_lstdelone(node, del);
	}
	*lst = NULL;
}
