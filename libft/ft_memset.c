#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;
	unsigned char	val;

	p = (unsigned char *)s;
	val = (unsigned char)c;
	i = 0;
	while (n > 0)
	{
		p[i] = val;
		i++;
		n--;
	}
	return (p);
}
